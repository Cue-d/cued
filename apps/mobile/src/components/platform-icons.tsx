/** Map of platform to icon component */
const PLATFORM_ICON_MAP: Record<ActionPlatform, React.ComponentType<IconProps>> = {
  imessage: IMessageColorIcon,
  slack: SlackColorIcon,
  linkedin: LinkedInIcon,
  twitter: TwitterIcon,
  signal: SignalIcon,
  whatsapp: WhatsAppIcon,
};

export interface PlatformIconProps {
  platform: ActionPlatform | string;
  size?: number;
  color?: string;
}

/**
 * Renders the correct brand SVG for a given platform.
 * If no color is provided, defaults to the platform's brand color from PLATFORM_CONFIG.
 */
export function PlatformIcon({ platform, size = 16, color }: PlatformIconProps) {
  const Icon = PLATFORM_ICON_MAP[platform as ActionPlatform];
  if (!Icon) return null;
  const resolvedColor = color ?? PLATFORM_CONFIG[platform as ActionPlatform]?.color ?? "#8E8E93";
  return <Icon size={size} color={resolvedColor} />;
}
